package com.hd.sys.core.tags.modal;

/**
 * 后台向前台返回JSON，用于select
 * 
 * @author xf 日期：2014-12-3
 */
public class Select {

	private String id;// ID
	private String text;// 文本
	private boolean selected;// 是否选中
	private boolean disabled;// 是否可选
	private boolean dataDivider;// 启用分割线
	private String dataSubtext;// 副文本
	private String dataIcon;// 图标
	private String dataContent;// 自定义HTML数据内容
	private Object data;

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public boolean isSelected() {
		return selected;
	}

	public void setSelected(boolean selected) {
		this.selected = selected;
	}

	public boolean isDisabled() {
		return disabled;
	}

	public void setDisabled(boolean disabled) {
		this.disabled = disabled;
	}

	public boolean isDataDivider() {
		return dataDivider;
	}

	public void setDataDivider(boolean dataDivider) {
		this.dataDivider = dataDivider;
	}

	public String getDataSubtext() {
		return dataSubtext;
	}

	public void setDataSubtext(String dataSubtext) {
		this.dataSubtext = dataSubtext;
	}

	public String getDataIcon() {
		return dataIcon;
	}

	public void setDataIcon(String dataIcon) {
		this.dataIcon = dataIcon;
	}

	public String getDataContent() {
		return dataContent;
	}

	public void setDataContent(String dataContent) {
		this.dataContent = dataContent;
	}

}
